using System.Collections.Generic;
using System.IO;
using System.Text;
using Flock.Models;
using UnityEngine;

namespace Flock.Editor.Codegen
{
    internal static class ConfigAccessorEmitter
    {
        public static int Emit(
            IList<GameConfigSchema> configs,
            IReadOnlyDictionary<string, string> configClassNamesById,
            string outputDir)
        {
            if (!Directory.Exists(outputDir))
                Directory.CreateDirectory(outputDir);

            StringBuilder sb = new StringBuilder();
            sb.AppendLine("// <auto-generated>");
            sb.AppendLine("//   Generated by Flock Codegen. Do not edit by hand.");
            sb.AppendLine("// </auto-generated>");
            sb.AppendLine("using System.Collections.Generic;");
            sb.AppendLine("using System.Threading;");
            sb.AppendLine("using System.Threading.Tasks;");
            sb.AppendLine("using Flock.Providers;");
            sb.AppendLine();
            sb.AppendLine("namespace Flock.Generated.Configs");
            sb.AppendLine("{");
            sb.AppendLine("    public static class FlockConfigProviderExtensions");
            sb.AppendLine("    {");

            int methodCount = 0;
            HashSet<string> usedMethodNames = new HashSet<string>();

            foreach (GameConfigSchema c in configs)
            {
                if (string.IsNullOrEmpty(c.Id) || string.IsNullOrEmpty(c.Name)) continue;
                if (!configClassNamesById.TryGetValue(c.Id, out string className)) continue;

                string methodName = CodeGenNamingHelpers.UnDuplicate("Get" + CodeGenNamingHelpers.ToPascalCase(c.Name) + "Async", usedMethodNames);

                sb.AppendLine($"        public static async Task<{className}> {methodName}(this FlockConfigProvider provider, CancellationToken cancellationToken = default)");
                sb.AppendLine("        {");
                sb.AppendLine($"            List<{className}> patches = await provider.GetBySchemaAsync<{className}>({className}.SourceId, cancellationToken);");
                sb.AppendLine($"            return patches.Count > 0 ? patches[0] : default;");
                sb.AppendLine("        }");
                sb.AppendLine();
                methodCount++;
            }

            sb.AppendLine("    }");
            sb.AppendLine("}");

            File.WriteAllText(Path.Combine(outputDir, "FlockConfigProviderExtensions.g.cs"), sb.ToString());
            Debug.Log($"[Flock Codegen] Config accessors: emitted {methodCount}.");
            return methodCount;
        }
    }
}
